import { useState, useEffect, useCallback } from 'react';
import { User, Session } from '@supabase/supabase-js';
import { supabase, Profile } from '../lib/supabase';

export function useAuth() {
  const [user, setUser] = useState<User | null>(null);
  const [profile, setProfile] = useState<Profile | null>(null);
  const [loading, setLoading] = useState(true);
  const [session, setSession] = useState<Session | null>(null);
  const [initialized, setInitialized] = useState(false);

  // Fonction pour nettoyer l'√©tat lors de la d√©connexion
  const clearAuthState = useCallback(() => {
    console.log('üßπ Nettoyage de l\'√©tat d\'authentification');
    setUser(null);
    setProfile(null);
    setSession(null);
  }, []);

  // Fonction pour r√©cup√©rer le profil utilisateur
  const fetchProfile = useCallback(async (userId: string) => {
    try {
      console.log('üë§ R√©cup√©ration du profil pour l\'utilisateur:', userId);
      
      const { data, error } = await supabase
        .from('profiles')
        .select('*')
        .eq('user_id', userId)
        .single();

      if (error) {
        console.error('‚ùå Erreur lors de la r√©cup√©ration du profil:', {
          message: error.message,
          code: error.code,
          details: error.details,
          hint: error.hint,
          userId
        });
        
        // Si le profil n'existe pas, on peut essayer de le cr√©er
        if (error.code === 'PGRST116') {
          console.warn('‚ö†Ô∏è Profil non trouv√© pour l\'utilisateur:', userId);
          console.log('üí° Suggestion: V√©rifiez que le profil a √©t√© cr√©√© lors de l\'inscription');
        }
        
        throw error;
      }

      if (!data) {
        console.warn('‚ö†Ô∏è Aucune donn√©e de profil retourn√©e pour l\'utilisateur:', userId);
        throw new Error('Profil utilisateur introuvable');
      }

      console.log('‚úÖ Profil r√©cup√©r√© avec succ√®s:', {
        id: data.id,
        email: data.email,
        full_name: data.full_name,
        role: data.role,
        is_active: data.is_active
      });

      // V√©rifier si le compte est actif
      if (!data.is_active) {
        console.warn('‚ö†Ô∏è Compte utilisateur d√©sactiv√©:', data.email);
        throw new Error('Votre compte a √©t√© d√©sactiv√©. Contactez l\'administrateur.');
      }

      setProfile(data);
      return data;
    } catch (error) {
      console.error('‚ùå Erreur inattendue lors de la r√©cup√©ration du profil:', error);
      throw error;
    }
  }, []);

  // Fonction pour g√©rer les changements de session
  const handleSessionChange = useCallback(async (newSession: Session | null) => {
    console.log('üîÑ Changement de session d√©tect√©:', newSession ? 'Session active' : 'Pas de session');
    
    setSession(newSession);
    setUser(newSession?.user ?? null);

    if (newSession?.user) {
      try {
        await fetchProfile(newSession.user.id);
      } catch (error) {
        console.error('‚ùå Impossible de r√©cup√©rer le profil, d√©connexion de l\'utilisateur');
        // En cas d'erreur de profil, on d√©connecte l'utilisateur
        await supabase.auth.signOut();
        clearAuthState();
      }
    } else {
      clearAuthState();
    }
  }, [fetchProfile, clearAuthState]);

  // Initialisation de l'authentification
  useEffect(() => {
    let mounted = true;

    const initializeAuth = async () => {
      try {
        console.log('üöÄ Initialisation de l\'authentification...');
        
        // R√©cup√©rer la session actuelle
        const { data: { session }, error } = await supabase.auth.getSession();
        
        if (error) {
          console.error('‚ùå Erreur lors de la r√©cup√©ration de la session initiale:', {
            message: error.message,
            status: error.status
          });
          throw error;
        }

        if (mounted) {
          console.log('üìã Session initiale:', session ? 'Utilisateur connect√©' : 'Aucune session');
          await handleSessionChange(session);
          setInitialized(true);
        }
      } catch (error) {
        console.error('‚ùå Erreur lors de l\'initialisation de l\'authentification:', error);
        if (mounted) {
          clearAuthState();
          setInitialized(true);
        }
      } finally {
        if (mounted) {
          setLoading(false);
        }
      }
    };

    initializeAuth();

    // √âcouter les changements d'authentification
    const { data: { subscription } } = supabase.auth.onAuthStateChange(
      async (event, session) => {
        if (!mounted) return;

        console.log('üîî √âv√©nement d\'authentification:', event, session ? 'Session active' : 'Pas de session');
        
        try {
          switch (event) {
            case 'SIGNED_IN':
              console.log('‚úÖ Utilisateur connect√©');
              await handleSessionChange(session);
              break;
              
            case 'SIGNED_OUT':
              console.log('üëã Utilisateur d√©connect√©');
              await handleSessionChange(null);
              break;
              
            case 'TOKEN_REFRESHED':
              console.log('üîÑ Token rafra√Æchi');
              await handleSessionChange(session);
              break;
              
            case 'USER_UPDATED':
              console.log('üë§ Utilisateur mis √† jour');
              await handleSessionChange(session);
              break;
              
            default:
              console.log('üìù √âv√©nement d\'authentification:', event);
              await handleSessionChange(session);
          }
        } catch (error) {
          console.error('‚ùå Erreur lors du traitement de l\'√©v√©nement d\'authentification:', error);
          if (mounted) {
            clearAuthState();
          }
        }
      }
    );

    return () => {
      mounted = false;
      subscription.unsubscribe();
    };
  }, [handleSessionChange, clearAuthState]);

  const signIn = async (email: string, password: string) => {
    try {
      console.log('üîê Tentative de connexion pour:', email);
      setLoading(true);
      
      const { data, error } = await supabase.auth.signInWithPassword({
        email: email.trim().toLowerCase(),
        password,
      });
      
      if (error) {
        console.error('‚ùå Erreur de connexion:', {
          message: error.message,
          status: error.status,
          email
        });

        // Messages d'erreur personnalis√©s
        let userMessage = 'Erreur de connexion';
        switch (error.message) {
          case 'Invalid login credentials':
            userMessage = 'Email ou mot de passe incorrect';
            break;
          case 'Email not confirmed':
            userMessage = 'Veuillez confirmer votre email avant de vous connecter';
            break;
          case 'Too many requests':
            userMessage = 'Trop de tentatives de connexion. Veuillez r√©essayer plus tard';
            break;
          default:
            userMessage = error.message;
        }
        
        return { data, error: { ...error, message: userMessage } };
      }

      console.log('‚úÖ Connexion r√©ussie pour:', email);
      console.log('üìã Donn√©es de connexion:', {
        userId: data.user?.id,
        email: data.user?.email,
        sessionId: data.session?.access_token ? 'Token pr√©sent' : 'Pas de token'
      });
      
      return { data, error: null };
    } catch (error) {
      console.error('‚ùå Erreur inattendue lors de la connexion:', error);
      return { 
        data: null, 
        error: { 
          message: 'Une erreur inattendue s\'est produite lors de la connexion' 
        } 
      };
    } finally {
      setLoading(false);
    }
  };

  const signOut = async () => {
    try {
      console.log('üö™ Tentative de d√©connexion...');
      setLoading(true);
      
      const { error } = await supabase.auth.signOut();
      
      if (error) {
        console.error('‚ùå Erreur lors de la d√©connexion:', {
          message: error.message,
          status: error.status
        });
        return { error };
      }

      console.log('‚úÖ D√©connexion r√©ussie');
      clearAuthState();
      
      return { error: null };
    } catch (error) {
      console.error('‚ùå Erreur inattendue lors de la d√©connexion:', error);
      return { 
        error: { 
          message: 'Une erreur inattendue s\'est produite lors de la d√©connexion' 
        } 
      };
    } finally {
      setLoading(false);
    }
  };

  const signUp = async (email: string, password: string, fullName: string, role: string) => {
    try {
      console.log('üìù Tentative d\'inscription pour:', email);
      setLoading(true);
      
      const { data, error } = await supabase.auth.signUp({
        email: email.trim().toLowerCase(),
        password,
        options: {
          data: {
            full_name: fullName.trim(),
            role: role
          }
        }
      });

      if (error) {
        console.error('‚ùå Erreur lors de l\'inscription:', {
          message: error.message,
          status: error.status,
          email
        });
        return { data, error };
      }

      if (data.user) {
        console.log('‚úÖ Utilisateur cr√©√©, cr√©ation du profil...');
        
        // Cr√©er le profil utilisateur
        const { error: profileError } = await supabase
          .from('profiles')
          .insert([
            {
              user_id: data.user.id,
              email: email.trim().toLowerCase(),
              full_name: fullName.trim(),
              role: role as any,
              is_active: true
            },
          ]);

        if (profileError) {
          console.error('‚ùå Erreur lors de la cr√©ation du profil:', {
            message: profileError.message,
            code: profileError.code,
            details: profileError.details,
            userId: data.user.id
          });
          
          // Supprimer l'utilisateur si la cr√©ation du profil √©choue
          await supabase.auth.admin.deleteUser(data.user.id);
          
          return { 
            data: null, 
            error: { 
              message: 'Erreur lors de la cr√©ation du profil utilisateur' 
            } 
          };
        }

        console.log('‚úÖ Profil cr√©√© avec succ√®s pour:', email);
      }

      return { data, error: null };
    } catch (error) {
      console.error('‚ùå Erreur inattendue lors de l\'inscription:', error);
      return { 
        data: null, 
        error: { 
          message: 'Une erreur inattendue s\'est produite lors de l\'inscription' 
        } 
      };
    } finally {
      setLoading(false);
    }
  };

  // Fonction pour rafra√Æchir le profil
  const refreshProfile = useCallback(async () => {
    if (user?.id) {
      try {
        await fetchProfile(user.id);
      } catch (error) {
        console.error('‚ùå Erreur lors du rafra√Æchissement du profil:', error);
      }
    }
  }, [user?.id, fetchProfile]);

  // Fonction pour v√©rifier si la session est valide
  const isSessionValid = useCallback(() => {
    if (!session) return false;
    
    const now = Math.floor(Date.now() / 1000);
    const expiresAt = session.expires_at || 0;
    
    // V√©rifier si la session expire dans moins de 5 minutes
    const isExpiringSoon = (expiresAt - now) < 300;
    
    if (isExpiringSoon) {
      console.warn('‚ö†Ô∏è Session expire bient√¥t, rafra√Æchissement recommand√©');
    }
    
    return expiresAt > now;
  }, [session]);

  return {
    user,
    profile,
    session,
    loading: loading || !initialized,
    initialized,
    signIn,
    signOut,
    signUp,
    refreshProfile,
    isSessionValid,
  };
}